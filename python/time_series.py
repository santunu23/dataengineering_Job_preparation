import pandas as pd
import numpy as np



#Datetime Index: প্যান্ডাসে যখন আমরা কোনো ডেট-টাইম কলামকে মেইন ইনডেক্স (Index) বানিয়ে ফেলি, 
#তখন প্যান্ডাস ওই টেবিলকে সাধারণ টেবিল থেকে সুপার-ফাস্ট Time Series টেবিলে রূপান্তর করে।

#Resampling (ডাউনস্যাম্পলিং): ধরো তোমার ডাটাবেসে প্রতিদিনের সেলস ডেটা জমা হচ্ছে। এখন ড্যাশবোর্ডের 
#জন্য তোমার দরকার প্রতি সপ্তাহের মোট সেলস কত। প্যান্ডাসের resample('W') ফাংশনটি পুরো মাসের বা 
#বছরের ডেটাকে অটোমেটিক সপ্তাহে সপ্তাহে ভাগ করে সামারি করে দেয়।


# ২০২৬ সালের জুনের ১ তারিখ থেকে ১০ তারিখ পর্যন্ত প্রতিদিনের ডেট রেঞ্জ তৈরি করা
date_range=pd.date_range(start='2026-06-01', end='2026-06-10', freq='D')

# একটি ডামি ট্রানজেকশন ডেটাফ্রেম
ts_data = {
    'transaction_count': [120, 150, 90, 200, 250, 180, 300, 110, 170, 220],
    'amount_usd': [5000, 6500, 4200, 8000, 11000, 7500, 15000, 4900, 7100, 9500]
}

df=pd.DataFrame(ts_data, index=date_range[:10])  # প্রথম ১০ দিনের জন্য ডেটাফ্রেম তৈরি করা
print("--- ১. মূল টাইম-সিরিজ ডেটাসেট (Datetime Index সহ) ---")
print(df)
print("\n" + "="*50 + "\n")


# লজিক: শুধু ২০২৬ সালের জুনের ৩ তারিখ থেকে জুনের ৭ তারিখের ডেটা ফিল্টার করো
df_filtered = df.loc['2026-06-03':'2026-06-07']

print("--- ২. জুনের ৩ থেকে ৭ তারিখের ফিল্টারড ডেটা ---")
print(df_filtered)
print("\n" + "="*50 + "\n")

# '5D' মানে প্রতি ৫ দিনের ডেটা একসাথে গ্রুপ হবে এবং .sum() দিয়ে যোগ হবে
df_resampled=df.resample('5D').sum()

print("--- ৩. প্রতি ৫ দিনের সামারি রিপোর্ট (Resampled) ---")
print(df_resampled)


