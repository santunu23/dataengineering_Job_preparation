# প্রশ্ন: তোমাকে একটি অ্যারে দেওয়া আছে nums = [2, 11, 7, 15] এবং একটি target = 9।
# তোমাকে এমন দুটি সংখ্যার ইনডেক্স (Index/পজিশন) খুঁজে বের করতে হবে, যাদের যোগফল হবে ৯।



def two_sum(nums, target):
    seen = {} # এখানে আমরা মেমরি রাখবো (সংখ্যা: তার ইনডেক্স)
    
    for i, num in enumerate(nums): # i হলো ইনডেক্স, num হলো আসল সংখ্যা
        remaining = target - num   # টার্গেট মিলতে আর কত লাগবে
        
        # ১. যদি এই বাকি অংশটা (remaining) আমাদের ডিকশনারিতে আগে থেকেই থাকে
        if remaining in seen:
            # তবে আগের সংখ্যার ইনডেক্স এবং বর্তমান ইনডেক্স [i] রিটার্ন করো
            return [seen[remaining], i]
        
        # ২. যদি ডিকশনারিতে না থাকে, তবে বর্তমান সংখ্যা এবং তার ইনডেক্স ডিকশনারিতে সেভ করো
        # ---- এখানে তোমার কোড লেখো ----
        else:
            # seen ডিকশনারিতে 'num' কে Key এবং তার ইনডেক্স 'i' কে Value হিসেবে সেট করো
            seen[num] = i
            
    return []

# টেস্ট করার জন্য:
print(two_sum([2, 11, 7, 15], 9)) # আউটপুট [0, 2] আসা উচিত (কারণ ২ আর ৭ এর ইনডেক্স ০ আর ২)
